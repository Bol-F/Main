"""
The print() function in Python is used to output information to the console (or terminal).
It displays text, variables, or any other information you pass to it.
The purpose of print() is to give you feedback on what's happening in your program,
which is super helpful for debugging and understanding your code’s behavior.
"""

print("Hello World")





"""
In Python, assigning values to variables means giving a name to some data so you can use it later.
You do this using the = sign.
"""

x = 5        # x now holds the value 5
name = "Bob"  # name now holds the string "Bob"
price = 19.99 # price holds the float value 19.99

"""
The variable name (like x, name, price) is on the left side.
The value (like 5, "Bob", 19.99) is on the right side.
Python saves the value in the variable, and you can use it later in the program.
"""





"""
In Python, input() is a built-in function that allows you to get data from the user.
When you use input(), it pauses the program and waits for the user to type something on the keyboard.
After the user presses Enter, the program continues, and whatever the user typed is saved as a string (text).
"""

surname = input("What is your surname? ")
print(surname)

"""
When the program runs, it asks, "What's your surname?"
The user types their name (like "Alice") and presses Enter.
The program then says, "Alice"
"""

